#Números inteiros
n1 = int(input('Coloca um número aí: '))
n2 = int(input('Coloca outro número aí: '))
soma = n1 + n2
print(f'A soma entre os valores {n1} e {n2} é: {soma}')

#Números reais
n1 = float(input('Coloca um número aí: '))
n2 = float(input('Coloca outro número aí: '))
soma = n1 + n2
print(f'A soma dos valores é: {soma}')

